package com.zilinli.staybooking.model;
//**********************************************************************************************************************
// * Includes
//**********************************************************************************************************************


import javax.persistence.*;
import java.io.Serializable;

//**********************************************************************************************************************
// * Class definition
//**********************************************************************************************************************
@Entity
@Table(name = "stay_reserved_date")
public class StayReservedDate implements Serializable {

//**********************************************************************************************************************
// * Class constructors
//**********************************************************************************************************************

    public StayReservedDate() { }

    public StayReservedDate(StayReservedDateKey id, Stay stay) {
        this.id = id;
        this.stay = stay;
    }
//**********************************************************************************************************************
// * Public methods
//**********************************************************************************************************************
    public StayReservedDateKey getId() {
        return id;
    }

    public Stay getStay() {
        return stay;
    }
//**********************************************************************************************************************
// * Protected methods
//**********************************************************************************************************************

//**********************************************************************************************************************
// * Private methods
//**********************************************************************************************************************

//**********************************************************************************************************************
// * Private attributes
//**********************************************************************************************************************

    @EmbeddedId
    private StayReservedDateKey id;

    @MapsId("stay_id")
    @ManyToOne
    private Stay stay;

}
